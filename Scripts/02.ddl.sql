-- 접속한 계정의 테이블별 컬럼 조회
SELECT *
  FROM  USER_TAB_COLUMNS 
 WHERE table_name IN ('MEMBER');
 
-- 접속한 계정의 테이블명 제약조건 검색
SELECT *
  FROM USER_CONS_COLUMNS 
 WHERE table_name IN ('MEMBER');